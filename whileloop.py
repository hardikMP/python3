maxIteration = int(input("Enter a number  :"))
iteration = 0
while(iteration < maxIteration):
    iteration = iteration + 1
    if iteration == 1:
        print("Hello world for the", iteration , "st time")
    elif iteration == 2:
        print("Hello world for the", iteration , "nd time")
    elif iteration == 3:
        print("Hello world for the", iteration , "rd time")
    else:
        print("Hello world for the", iteration , "th time")

while (maxIteration):
    print ('Given flag is really true!')
    maxIteration = 0
print ("Good bye!")